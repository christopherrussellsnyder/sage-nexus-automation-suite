
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Calendar, Users, DollarSign, TrendingUp } from 'lucide-react';

interface MonthlyPlanProps {
  data: any;
}

const MonthlyPlan = ({ data }: MonthlyPlanProps) => {
  const monthlyPlan = data.monthlyPlan || [];

  if (!monthlyPlan || monthlyPlan.length === 0) {
    return (
      <Card>
        <CardHeader>
          <CardTitle>AI-Generated 30-Day Content Calendar</CardTitle>
          <CardDescription>
            AI content calendar not available. Please regenerate the report.
          </CardDescription>
        </CardHeader>
        <CardContent>
          <p className="text-muted-foreground">No AI-generated monthly plan found in the current report.</p>
        </CardContent>
      </Card>
    );
  }

  return (
    <Card>
      <CardHeader>
        <CardTitle className="flex items-center space-x-2">
          <Calendar className="h-5 w-5" />
          <span>AI-Generated 30-Day Content Calendar</span>
        </CardTitle>
        <CardDescription>
          Day-by-day content strategy generated by AI based on your business analysis
        </CardDescription>
      </CardHeader>
      <CardContent>
        <div className="space-y-4 max-h-96 overflow-y-auto">
          {monthlyPlan.slice(0, 30).map((day: any, index: number) => (
            <div key={index} className="border rounded-lg p-4">
              <div className="flex items-center justify-between mb-3">
                <div className="flex items-center space-x-2">
                  <Badge variant="outline">Day {day.day || index + 1}</Badge>
                  <Badge variant={day.contentType === 'ad' ? 'default' : 'secondary'}>
                    {day.contentType || 'content'}
                  </Badge>
                  <span className="text-sm font-medium">{day.platform || 'Platform'}</span>
                </div>
                {day.expectedMetrics?.reach && (
                  <div className="text-xs text-muted-foreground">
                    Est. {day.expectedMetrics.reach.toLocaleString()} reach
                  </div>
                )}
              </div>
              
              <div className="space-y-2">
                <div>
                  <h4 className="font-semibold text-sm mb-1">
                    {day.hook || 'AI Hook Not Generated'}
                  </h4>
                  <p className="text-xs text-muted-foreground">
                    {day.body || 'AI body content not generated'}
                  </p>
                </div>
                
                <div className="flex items-center justify-between">
                  <Badge variant="outline">{day.cta || 'CTA'}</Badge>
                  {day.expectedMetrics?.cost && (
                    <div className="text-xs text-muted-foreground">
                      ${day.expectedMetrics.cost}
                    </div>
                  )}
                </div>
                
                {day.strategicReasoning && (
                  <div className="bg-blue-50 p-2 rounded text-xs">
                    <strong>AI Strategy:</strong> {day.strategicReasoning}
                  </div>
                )}
                
                {day.expectedMetrics && (
                  <div className="grid grid-cols-4 gap-2 text-xs text-center pt-2 border-t">
                    <div className="flex items-center space-x-1">
                      <Users className="h-3 w-3" />
                      <span>{day.expectedMetrics.reach || 'N/A'}</span>
                    </div>
                    <div className="flex items-center space-x-1">
                      <TrendingUp className="h-3 w-3" />
                      <span>{day.expectedMetrics.engagement || 'N/A'}</span>
                    </div>
                    <div className="flex items-center space-x-1">
                      <DollarSign className="h-3 w-3" />
                      <span>${day.expectedMetrics.cost || 'N/A'}</span>
                    </div>
                    <div>
                      <span>{day.expectedMetrics.conversions || 'N/A'} conv</span>
                    </div>
                  </div>
                )}
              </div>
            </div>
          ))}
        </div>
        
        <div className="mt-4 p-3 bg-green-50 rounded border border-green-200">
          <p className="text-sm text-green-800">
            <strong>AI-Generated Content Calendar:</strong> Each day's content is strategically planned by AI 
            based on your business goals, audience behavior patterns, and optimal posting schedules for maximum engagement.
          </p>
        </div>
      </CardContent>
    </Card>
  );
};

export default MonthlyPlan;
